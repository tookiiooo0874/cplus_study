#include <iostream>
using namespace std;

int main() {
    cout << "2 + 2 = " << 2 + 2 << endl;
    cout << "1 - 5 = " << 1 - 5 << endl;
    cout << "2 * 9 = " << 2 * 9 << endl;
    cout << "9 / 3 = " << 9 / 3 << endl;
    cout << "9 / 2 = " << 9 / 2 << endl;

    cout << "9 % 2 = " << 9 % 2 << endl;
    cout << "2 + 2 * 4 = " << 2 + 2 * 4 << endl;
    cout << "(1 - 5) / 2 = " << (1 - 5) / 2 << endl;
}
